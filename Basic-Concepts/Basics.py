# Scripting way of writing the code:
print('Scripting way of writing the code: Hello World')

# Procedural way of writing the code:
def message():
    print('Procedural way of writing the code: Hello World')
message()

# Object Oriented way of writing the code:
class Demo:
    def instance_method(self):
        print('Object Oriented way of writing the code: Hello World')
d1 = Demo()
d1.instance_method()